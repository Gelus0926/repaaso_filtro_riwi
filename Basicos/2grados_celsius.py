#Convertir grados Celsius a Fahrenheit (F = C * 9/5 + 32).

celsius = int(input("Ingrese los grados celsius: "))

fahrenheit = (celsius * 9/5 ) + 32

print("Los grados fahrenheit son: ",fahrenheit)


