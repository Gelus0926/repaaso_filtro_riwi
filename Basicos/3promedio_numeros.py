#Calcular el promedio de tres números ingresados por el usuario.

numero1 = int(input("Digite el primer número: "))
numero2 = int(input("Digite el segundo número: "))
numero3 = int(input("Digite el tercer número: "))

promedio = int((numero1 + numero2 + numero3) / 3)

print(f"El promedio de los tres números ingresados es: {promedio}")